export function searchKnowledge(query: string): KnowledgeChunk[] {
  const q = query.toLowerCase()
  const scored = knowledgeBase.map((chunk) => {
    let score = 0
    // keyword match
    for (const kw of chunk.keywords) {
      if (q.includes(kw.toLowerCase())) score += 3
    }
    // title match
    if (chunk.title.toLowerCase().split('').some((c) => q.includes(c))) {
      const titleWords = chunk.title.toLowerCase().split(/\s+/)
      for (const tw of titleWords) {
        if (tw.length > 1 && q.includes(tw)) score += 2
      }
    }
    // content substring match
    const contentLower = chunk.content.toLowerCase()
    const queryWords = q.split(/\s+/).filter((w) => w.length > 1)
    for (const qw of queryWords) {
      if (contentLower.includes(qw)) score += 1
    }
    return { chunk, score }
  })

  return scored
    .filter((s) => s.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 5)
    .map((s) => s.chunk)
}
